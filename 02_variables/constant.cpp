#include<iostream>
using namespace std;
int main() {
    // Declare and initialize constants
    const int MAX_AGE = 100; // Constant integer
    const double PI = 3.14159; // Constant double
    const float GRAVITY = 9.81f; // Constant float
    const char GRADE = 'A'; // Constant character
    const string COUNTRY = "India"; // Constant string

    // Output the constants
    cout << "Max Age: " << MAX_AGE << endl;
    cout << "Value of PI: " << PI << endl;
    cout << "Gravity: " << GRAVITY << endl;
    cout << "Grade: " << GRADE << endl;
    cout << "Country: " << COUNTRY << endl;

    return 0;
}