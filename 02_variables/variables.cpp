#include<iostream>
using namespace std;
int main() {
    // Declare and initialize variables
    int age = 25; // Integer variable
    double height = 5.9; // Double variable
    float weight = 65.5f; // Float variable
    char initial = 'A'; // Character variable
    string name = "Aditya"; // String variable
    string city;
    // Output the variables
    cout << "Name: " << name << endl;
    cout << "Initial: " << initial << endl;
    cout << "Age: " << age << endl;
    cout << "Height: " << height << endl;
    cout << "Weight: " << weight << endl;
    city = "New York"; // Assigning a value to the city variable
    cout << "City: " << city << endl;
    return 0;
}